/*
 *Die Klasse Rechnen.class schreibt Text und Zahlen
 *auf die Kommandozeile. 
 */
class Rechnen 
{
	public static void main(String args [])
	{ 
		System.out.print("Mit dem Befehl \'print\' und \'println\' \nkann");
		System.out.println(" man schreiben und auch rechnen:");
		System.out.println("4 * 5 = \t" + 4 * 5);
		System.out.println("4 / 5 = \t" + 4.0 / 5);
		System.out.println("4 + 5 = \t" + (4 + 5));
		System.out.println("4 - 5 = \t" + (4 - 5));
		
	}
}